int	ft_str_is_alpha(char *str)
{
	char	c;
	int		i;
	int		output;

	output = 1;
	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c < 65 || (c > 90 && c < 97) || c > 122)
		{
			output = 0;
		}
		i++;
	}
	return (output);
}
