char	*ft_strupcase(char *str)
{
	char	c;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c > 96 && c < 123)
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
