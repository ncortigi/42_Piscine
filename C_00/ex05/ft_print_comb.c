#include<unistd.h>

void	ft_print_comb(void)
{
	char	n_1;
	char	n_2;
	char	n_3;

	n_1 = 48;
	while (n_1 <= 57)
	{
		n_2 = n_1 + 1;
		while (n_2 <= 57)
		{
			n_3 = n_2 + 1;
			while (n_3 <= 57)
			{
				write(1, &n_1, 1);
				write(1, &n_2, 1);
				write(1, &n_3, 1);
				if (n_1 < 55)
					write(1, ", ", 2);
				n_3++;
			}
			n_2++;
		}
		n_1++;
	}
}
