int	ft_is_prime(int nb)
{
	int	num;

	num = 2;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	while (num <= nb / num)
	{
		if ((nb % num) == 0)
			return (0);
		num++;
	}
	return (1);
}
